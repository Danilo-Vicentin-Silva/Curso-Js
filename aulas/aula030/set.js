// Conjuntos (ou Set) é uma estrutura de dados onde seus valores são únicos e não ordenados.
// Tem um funcionamento similar aos conjuntos da matemática. Surgiu apartir do EcmanScript 2015

// Implementação
const conjunto = new Set()

// Métodos
conjunto.has() // Procura elementos e devolve booleano
conjunto.add() // Adiciona elementos não existentes
conjunto.delete() // Remove elementos existentes
conjunto.clear() // Limpa todos os elementos da estrutura
conjunto.size() // Verifica quantidade de elementos
conjunto.values() // Loga todos os valores da estrutura

